const FILES = [
  join(__dirname, '../src/data/questions/mcq/islam/medium.ts'),
  join(__dirname, '../src/data/questions/mcq/islam/hard.ts'),
  join(__dirname, 'gen-islam-medium.mjs'),
  join(__dirname, 'gen-islam-hard.mjs'),
];

for (const file of FILES) {
  let content = readFileSync(file, 'utf8');
  let count = 0;
  for (const [from, to] of REPLACEMENTS) {
    const parts = content.split(from);
    if (parts.length > 1) {
      count += parts.length - 1;
      content = parts.join(to);
    }
  }
  writeFileSync(file, content);
  console.log(`${file.split(/[/\\]/).pop()}: ${count} replacements`);
}
